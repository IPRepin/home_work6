class Student:
    def __init__(self, name, surname, gender):
        self.name = name
        self.surname = surname
        self.gender = gender
        self.finished_courses = []
        self.courses_in_progress = []
        self.grades = {}

    def rate_lecture(self, lecturer, course, grade):
        if isinstance(lecturer, Lecturer) and course in self.courses_attached:
            if course in lecturer.grades_lecturer:
                lecturer.grades_lecturer[course] += [grade]
            else:
                lecturer.grades_lecturer[course] = [grade]
        else:
            return 'Ошибка'




class Mentor:
    def __init__(self, name, surname):
        self.name = name
        self.surname = surname
        self.courses_attached = []


class Lecturer(Mentor):
    def rate_lecturer(self):
        grades_lecturer = {}
        return grades_lecturer

    def average_grades(self, grades_lecturer=None):
        rate_lst = list(grades_lecturer.values())
        avg_grades = sum(rate_lst) / len(rate_lst)
        return avg_grades

    def __str__(self):
        some_lecturer = f"Имя: {self.name}'\n'Фамилия: {self.surname}'\n'Средняя оценка за лекции: {self.average_grades()}"
        return some_lecturer

